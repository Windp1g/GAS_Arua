void AAuraCharacterBase::Die()
{
	Weapon->DetachFromComponent(FDetachmentTransformRules(EDetachmentRule::KeepWorld, true));
	MulticastHandleDeath();
}

void AAuraCharacterBase::MulticastHandleDeath_Implementation()
{
	UGameplayStatics::PlaySoundAtLocation(this, DeathSound, GetActorLocation(), GetActorRotation());
	
	Weapon->SetSimulatePhysics(true);
	Weapon->SetEnableGravity(true);
	Weapon->SetCollisionEnabled(ECollisionEnabled::Type::PhysicsOnly);

	GetMesh()->SetSimulatePhysics(true);
	GetMesh()->SetEnableGravity(true);
	GetMesh()->SetCollisionEnabled(ECollisionEnabled::Type::PhysicsOnly);
	GetMesh()->SetCollisionResponseToChannel(ECC_WorldStatic, ECollisionResponse::ECR_Block);

	GetCapsuleComponent()->SetCollisionEnabled(ECollisionEnabled::Type::NoCollision);
	Dissolve();
	bDead = true;
}

void AAuraCharacterBase::BeginPlay()
{
	Super::BeginPlay();
	
	CombatSocketMap.Add(FAuraGameplayTags::Get().CombatSocket_Weapon, WeaponTipSocketName);
	CombatSocketMap.Add(FAuraGameplayTags::Get().CombatSocket_RightHand, RightHandTipSocketName);
	CombatSocketMap.Add(FAuraGameplayTags::Get().CombatSocket_LeftHand, LeftHandTipSocketName);
	CombatSocketMap.Add(FAuraGameplayTags::Get().CombatSocket_Tail, TailTipSocketName);
}

FVector AAuraCharacterBase::GetCombatSocketLocation_Implementation(const FGameplayTag& MontageTag)
{
	if (CombatSocketMap.Contains(MontageTag))
	{
		FName SocketName = CombatSocketMap[MontageTag];
		if (MontageTag.MatchesTagExact(FAuraGameplayTags::Get().CombatSocket_Weapon) && IsValid(Weapon))
		{
			return Weapon->GetSocketLocation(SocketName);
		}
		else
		{
			return GetMesh()->GetSocketLocation(SocketName);
		}
	}
	return FVector();
}

